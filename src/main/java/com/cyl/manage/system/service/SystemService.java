	/**
	 * @Author : xuchang
	 * @Description : 获取在线用户
	 * @Param :
	 * @Return : java.util.Map<java.lang.String,java.lang.Object>
	 * @Date : 2018/4/8 18:05
	 */
	public Map<String,Object> getOnlineUsers(){
		Map<String,Object> map = Maps.newHashMap() ;
		Collection<Session> sessionLiST = getActiveSessions();
		Iterator<Session> it = sessionLiST.iterator();
		while(it.hasNext()){
			Session se = it.next() ;
			SimplePrincipalCollection simplePrincipalCollection =
					(SimplePrincipalCollection)se.getAttribute(DefaultSubjectContext.PRINCIPALS_SESSION_KEY);
			if(null != simplePrincipalCollection){
				SystemAuthorizingRealm.Principal primaryPrincipal = (SystemAuthorizingRealm.Principal)simplePrincipalCollection.getPrimaryPrincipal() ;
				if(null != primaryPrincipal){
					map.put(se.getId().toString(),primaryPrincipal);
				}
			}
		}
		return map ;
	}

